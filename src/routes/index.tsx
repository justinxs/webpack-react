const Home = React.lazy(() => import('@/pages/Home'));
const Dashboard = React.lazy(() => import('@/pages/dashboard/Index'));
const DashboardHome = React.lazy(() => import('@/pages/dashboard/DashboardHome'));
const Invoices = React.lazy(() => import('@/pages/invoices/Index'));
const Login = React.lazy(() => import('@/pages/user/Login'));
const NoFoundPage = React.lazy(() => import('@/pages/404'));
const Register = React.lazy(() => import('@/pages/user/register/index'));
const RegisterResult = React.lazy(() => import('@/pages/user/register-result/index'));

const createRouter = () => {
    let routes: RouteObject[] = [
        {
            path: "/",
            element: <Layout />,
            children: [
                { index: true, element: <Home /> },
                {
                    path: "dashboard/*",
                    element: <Dashboard />,
                    children: [
                        { index: true, element: <DashboardHome /> },
                        { path: "invoices/:invoiceId", element: <Invoices /> }
                    ]
                }
            ]
        },
        { path: "/user/login", element: <Login /> },
        { path: "/user/register", element: <Register /> },
        { path: "/user/register-result", element: <RegisterResult /> },
        { path: "*", element: <NoFoundPage /> }
    ];

    return useRoutes(routes);
};

export default createRouter;